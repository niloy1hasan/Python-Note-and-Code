# range(start, end, increment/decrement)
# range(end) - start from 0 and increment is 1

# print 0 to 9
for i in range(10):
    print(i)

# print 1 to 10
for i in range(1, 11):
    print(i)

# print 20 to 10
for i in range(20, 9, -1):
    print(i)

# character print from a string
string = "hello world"
for i in string:
    print(i)

# items print from a list
data = [3, 5, 3, 8, 4]
for i in data:
    print(i)

data2 = ["abc", "bcs", "ard"]
for i in data2:
    print(i)


