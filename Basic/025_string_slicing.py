# string slicing means cut a string from a starting point to end point
# python slicing:
# variableName[stringPoint:endPoint] //when endPoint found do not take endPoint on slicing
# variableName[:endPoint] // 0 index is starting point
# variableName[startingPoint:] //last index of string is end point
# negative point:
# negative point is count from end


string = "Hello, World!"
print(string)
print(string[2:5])
print(string[: 5])
print(string[3:])
print(string[-6:-2])