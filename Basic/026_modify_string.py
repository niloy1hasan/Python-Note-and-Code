a = "hello, world!"
print(a)

# convert into upper case - variable/string.upper()
# convert into lower case - variable/string.lower()
# it modify variable's data, not temporarily
print(a.upper())
print(a.lower())
print(a)

# remove whitespace - variable/string.strip()
string = "   hello     , world   !    "
print(string.strip())

# replace string/character - variable/string.replace(currentStringPart, newStringPart)
string = "Who are you?"
print(string.replace('Who', 'How'))



