# casting means convert a value into different datatype
# to convert data type use these methods

# int() - convert into integer
# float() - convert into flaoting point number
# str() - convert into string value
# bool() - convert into boolean - if original value not 0 than always true


floatValue = 34.3
intValue = int(floatValue)
stringValue = str(floatValue)
boolValue = bool(floatValue)
print(intValue)
print(stringValue)
print(type(stringValue))
print(boolValue)